SOURCE_TAG = "Мединский, История Древнего мира, 5 кл."


def base_meta(section: str) -> dict:
    return {"textbook": TEXTBOOK, "section": section}


def tags_base(
    paragraph: str,
    page: int,
    q_type: str,
    themes: list,
    concepts: list = None,
    entities: list = None,
    processes: list = None,
    periods: list = None,
    skills: list = None,
    geography: list = None,
    diff_pri: str = "easy",
    diff_sec: str = "easy",
) -> dict:
    d = diff_pri if diff_sec == diff_pri else f"{diff_pri}+{diff_sec}"
    return {
        "entities": entities or [],
        "concepts": concepts or [],
        "processes": processes or [],
        "periods": periods or [],
        "skills": skills or [],
        "geography": geography or [],
        "difficulty": d,
        "question_type": q_type,
        "grade": [5],
        "source": SOURCE_TAG,
        "themes": themes,
    }


def difficulty_pair(pri: str, sec: str) -> dict:
    return {"primary": pri, "secondary": sec}


def dump(name: str, meta: dict, questions: list) -> None:
    obj = {"meta": meta, "questions": questions}
    p = OUT / name
    p.write_text(json.dumps(obj, ensure_ascii=False, indent=2), encoding="utf-8")
    print("wrote", p)


