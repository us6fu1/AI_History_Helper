def _sc(
    paragraph: str,
    page: int,
    idx: int,
    sid: str,
    question: str,
    options: list,
    correct: str,
    explanation: str,
    themes: list,
    dp: str = "easy",
    ds: str = "easy",
    **tag_kw,
):
    return {
        "id": f"g5-p{sid}-{idx:03d}",
        "type": "single_choice",
        "question": question,
        "options": options,
        "correct_answers": [correct],
        "explanation": explanation,
        "source": {"paragraph": paragraph, "page": page},
        "difficulty": difficulty_pair(dp, ds),
        "tags": tags_base(paragraph, page, "single_choice", themes, diff_pri=dp, diff_sec=ds, **tag_kw),
    }


