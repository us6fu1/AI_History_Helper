def _match(
    paragraph: str,
    page: int,
    idx: int,
    sid: str,
    question: str,
    left_items: list,
    right_items: list,
    correct_pairs: list,
    explanation: str,
    themes: list,
    dp: str = "medium",
    ds: str = "hard",
    **tag_kw,
):
    return {
        "id": f"g5-p{sid}-{idx:03d}",
        "type": "matching",
        "question": question,
        "left_items": left_items,
        "right_items": right_items,
        "correct_pairs": correct_pairs,
        "explanation": explanation,
        "source": {"paragraph": paragraph, "page": page},
        "difficulty": difficulty_pair(dp, ds),
        "tags": tags_base(paragraph, page, "matching", themes, diff_pri=dp, diff_sec=ds, **tag_kw),
    }


def _chrono(
    paragraph: str,
    page: int,
    idx: int,
    sid: str,
    question: str,
    items: list,
    explanation: str,
    themes: list,
    dp: str = "medium",
    ds: str = "medium",
    **tag_kw,
):
    order = list(range(len(items)))
    return {
        "id": f"g5-p{sid}-{idx:03d}",
        "type": "chronology",
        "question": question,
        "items": items,
        "correct_order": order,
        "correct_answers": list(items),
        "explanation": explanation,
        "source": {"paragraph": paragraph, "page": page},
        "difficulty": difficulty_pair(dp, ds),
        "tags": tags_base(paragraph, page, "chronology", themes, diff_pri=dp, diff_sec=ds, **tag_kw),
    }


