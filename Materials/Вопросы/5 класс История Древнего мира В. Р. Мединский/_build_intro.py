def diff_obj(s: str) -> dict:
    if s == "easy":
        return {"primary": "easy", "secondary": "easy"}
    if s == "medium":
        return {"primary": "medium", "secondary": "medium"}
    if s == "hard":
        return {"primary": "hard", "secondary": "hard"}
    if s == "easy+medium":
        return {"primary": "easy", "secondary": "medium"}
    if s == "medium+hard":
        return {"primary": "medium", "secondary": "hard"}
    raise ValueError(s)


def tags(
    qtype: str,
    diff_slug: str,
    skills,
    concepts: list,
    entities=None,
    processes=None,
    periods=None,
    geography=None,
):
    entities = entities or []
    processes = processes or []
    periods = periods or []
    geography = geography or []
    if isinstance(skills, str):
        skills = [skills]
    return {
        "entities": entities,
        "concepts": concepts,
        "processes": processes,
        "periods": periods,
        "skills": skills,
        "geography": geography,
        "difficulty": diff_slug,
        "question_type": qtype,
        "grade": [5],
        "source": TB,
        "themes": ["Введение"],
    }


def emit_file(name: str, section: str, paragraph_key: str, questions: list) -> None:
    path = os.path.join(OUT, name)
    data = {"meta": {"textbook": TB, "section": section}, "questions": questions}
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    json.load(open(path, encoding="utf-8"))
    print("OK", name, len(questions))


